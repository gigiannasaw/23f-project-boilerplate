CREATE TABLE IF NOT EXISTS 'CafeConnect'.'Cafe' (
    owner_id int NOT NULL,
    time text NOT NULL,
    days text NOT NULL,
    website_link text,
    cafe_id int PRIMARY KEY,
    name text NOT NULL,
    street text NOT NULL,
    city text NOT NULL,
    zip int NOT NULL,
    state text NOT NULL,
    has_wifi boolean,
    has_outlets boolean,
    FOREIGN KEY (owner_id) REFERENCES BusinessOwner(owner_id)
);

CREATE TABLE IF NOT EXISTS Customer (
    customer_id int AUTO_INCREMENT PRIMARY KEY,
    first_name text NOT NULL,
    last_name text NOT NULL,
    `rank` int NOT NULL
);

CREATE TABLE IF NOT EXISTS Ratings (
    service_speed int,
    price int,
    noise int,
    options text,
    rating_id int AUTO_INCREMENT NOT NULL,
    customer_id int NOT NULL,
    cafe_id int NOT NULL,
    PRIMARY KEY(rating_id, cafe_id, customer_id),
    FOREIGN KEY (cafe_id) REFERENCES Cafe(cafe_id),
    FOREIGN KEY (customer_id) REFERENCES Customer(customer_id)
);

CREATE TABLE IF NOT EXISTS Student (
    customer_id int PRIMARY KEY,
    FOREIGN KEY (customer_id) REFERENCES Customer(customer_id)
);

CREATE TABLE IF NOT EXISTS Critic (
    customer_id int PRIMARY KEY,
    user_status text NOT NULL,
    FOREIGN KEY (customer_id) REFERENCES Customer(customer_id)
);

CREATE TABLE IF NOT EXISTS DailyDrinker (
    customer_id int PRIMARY KEY,
    FOREIGN KEY (customer_id) REFERENCES Customer(customer_id)
);

CREATE TABLE IF NOT EXISTS Promotion (
    cafe_id int NOT NULL,
    promo_id int NOT NULL,
    description text NOT NULL,
    title text,
    duration int,
    PRIMARY KEY (cafe_id, promo_id),
    FOREIGN KEY (cafe_id) REFERENCES Cafe(cafe_id)
);

CREATE TABLE IF NOT EXISTS Reviews (
    customer_id int NOT NULL,
    cafe_id int,
    review_id int NOT NULL,
    content text,
    PRIMARY KEY (customer_id, review_id),
    FOREIGN KEY (customer_id) REFERENCES Customer(customer_id),
    FOREIGN KEY (cafe_id) REFERENCES Cafe(cafe_id)
);

CREATE TABLE IF NOT EXISTS Invite (
    invite_id int AUTO_INCREMENT NOT NULL PRIMARY KEY,
    customer_id int NOT NULL,
    description text NOT NULL,
    cafe_id int NOT NULL,
    FOREIGN KEY (customer_id) REFERENCES Customer(customer_id),
    FOREIGN KEY (cafe_id) REFERENCES Cafe(cafe_id)
);


